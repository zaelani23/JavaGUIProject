    private void getAllPeserta() {
        String idPeserta, namaPeserta, peserta;
        String query = "SELECT * FROM peserta";
        try{
            pst = conn.prepareStatement(query);
            rs  = pst.executeQuery();
            while(rs.next()){
                idPeserta = rs.getString("id_peserta");
                namaPeserta = rs.getString("nama_peserta");
                
                peserta = idPeserta + " - " + namaPeserta;
                pesertaComboBox.addItem(peserta);
            }
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }        
    }

