    private void getAllNilai() {
        defaultTableNilai.setRowCount(0);
        int no = 1;
        int nilaiPeserta;
        String idPeserta, namaPeserta, namaSesi;
        String query = "SELECT hasil_test.*, peserta.nama_peserta, sesi_test.nama_sesi "
                + "FROM hasil_test, peserta, sesi_test "
                + "WHERE hasil_test.id_peserta = peserta.id_peserta AND hasil_test.id_sesi = sesi_test.id_sesi";
        
        try{
            pst = conn.prepareStatement(query);
            rs  = pst.executeQuery();
            while(rs.next()){
                idPeserta = rs.getString("id_peserta");
                namaPeserta = rs.getString("nama_peserta");
                namaSesi = rs.getString("nama_sesi");
                nilaiPeserta = rs.getInt("nilai");
                
                defaultTableNilai.addRow(new Object[] {no, idPeserta, namaPeserta, namaSesi, nilaiPeserta});
                no++;
            }
            tabelNilai.setModel(defaultTableNilai);
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }        
    }
        
