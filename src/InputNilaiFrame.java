    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String idSesi, idPeserta, sesiTest, peserta;
        int nilaiPeserta;
        String query = "CALL insert_hasil_test(?, ?, ?)";
        
        sesiTest = sesiTestComboBox.getSelectedItem().toString();
        peserta = pesertaComboBox.getSelectedItem().toString();
        
        idSesi = sesiTest.split(" - ")[0];
        idPeserta = peserta.split(" - ")[0];        
        
        if(nilaiField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi semua field!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            nilaiPeserta = Integer.parseInt(nilaiField.getText());
            try{
                pst = conn.prepareStatement(query);
                pst.setString(1, idSesi);
                pst.setString(2, idPeserta);
                pst.setInt(3, nilaiPeserta);
                if(pst.executeUpdate() != 0){
                    nilaiField.setText("");
                    JOptionPane.showMessageDialog(null, "Input nilai berhasil!", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
                    getAllNilai();
                }else{
                    JOptionPane.showMessageDialog(null, "Input nilai gagal!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

